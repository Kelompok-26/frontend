    },

    postTransaction() {
      this.loading = true

      const postTrans = {
        number: this.nohp,
        product_id: this.index,
      }
      const config = {
        method: 'post',
        url: `http://ec2-54-160-45-255.compute-1.amazonaws.com:8080/v1/user/${this.idUser}/transaction`,
        headers: {
          Authorization: `Bearer ${this.tokens}`,
        },
        data: postTrans,
        timeout: 2000,
